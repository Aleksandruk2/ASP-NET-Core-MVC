namespace Core.Models.Account;

/// <summary>
/// Модель запиту для ініціалізації процесу відновлення пароля користувача.
/// Використовується для передачі email-адреси, на яку буде надіслано
/// інструкції щодо скидання пароля.
/// </summary>
public class ForgotPasswordModel
{
    /// <summary>
    /// Email-адреса користувача, для якого потрібно розпочати процес
    /// відновлення пароля. На цю адресу буде надіслано лист
    /// з посиланням або кодом для скидання пароля.
    /// </summary>
    public string Email { get; set; } = string.Empty;
}

