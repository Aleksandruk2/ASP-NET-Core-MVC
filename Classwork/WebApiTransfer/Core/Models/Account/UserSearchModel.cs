namespace Core.Models.Account;

/// <summary>
/// Модель параметрів пошуку та пагінації користувачів.
/// Використовується для фільтрації результатів за іменем,
/// діапазоном дат та керування посторінковим виведенням даних.
/// </summary>
public class UserSearchModel
{
    /// <summary>
    /// Ім'я або частина імені користувача для пошуку.
    /// Якщо значення не задане, фільтрація за іменем не застосовується.
    /// </summary>
    public string? Name { get; set; }

    /// <summary>
    /// Початкова дата для фільтрації користувачів.
    /// Вибираються записи, створені не раніше вказаної дати.
    /// Якщо значення не задане, нижня межа діапазону не враховується.
    /// </summary>
    public DateTime? StartDate { get; set; }

    /// <summary>
    /// Кінцева дата для фільтрації користувачів.
    /// Вибираються записи, створені не пізніше вказаної дати.
    /// Якщо значення не задане, верхня межа діапазону не враховується.
    /// </summary>
    public DateTime? EndDate { get; set; }

    /// <summary>
    /// Номер сторінки для пагінації.
    /// За замовчуванням встановлено значення 1.
    /// </summary>
    public int Page { get; set; } = 1;

    /// <summary>
    /// Кількість елементів на одній сторінці.
    /// За замовчуванням встановлено значення 10.
    /// </summary>
    public int ItemPerPage { get; set; } = 10;
}
